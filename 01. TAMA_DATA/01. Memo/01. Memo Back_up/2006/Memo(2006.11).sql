SELECT RANK() OVER (ORDER BY A.POST_CD ASC, (SELECT C1 FROM T_MISCOMMON WHERE COMPANY_CD = '01'  
AND MISGROUP_ID = 'E104' AND MISCODE = A.DUTY GROUP BY C1) DESC, A.EMPLOYEE_NM ASC) NO, 
   (SELECT POST_NM FROM T_POST WHERE COMPANY_CD = A.COMPANY_CD AND POST_CD = A.POST_CD) AS 부서, 
   (SELECT C1 FROM T_MISCOMMON WHERE COMPANY_CD = '01' AND MISGROUP_ID = 'E104' AND MISCODE = A.DUTY GROUP BY C1) 직책, 
   A.EMPLOYEE_NM AS 성명,  
   (SELECT C1 FROM T_MISCOMMON WHERE COMPANY_CD = '01' AND MISGROUP_ID = 'E102' AND MISCODE = A.POSITION GROUP BY C1) 직위, 
   (SELECT EMAIL FROM T_USER WHERE COMPANY_CD = A.COMPANY_CD AND EMPLOYEE_ID = A.EMPLOYEE_ID AND USE_YN = 'Y') AS EMAIL주소,  
   A.EMAIL AS EMAIL주소_사원정보, A.MOBILE AS 휴대폰, A.RETIRE_DT AS 퇴사일 
FROM T_EMPLOYEE A, T_SCHOOLING B 
WHERE A.EMPLOYEE_ID = B.EMPLOYEE_ID 
  AND A.POST_CD IN ('D201', 'D602')       --부서코드(B201:전산팀) 
  AND B.SCHOOLONG_TYPE <>'04'          --고졸 제외 
  AND B.SCHOOL_NM <>'한국방송통신대학교' 
  AND A.RETIRE_DT IS NULL                 --퇴사일이 널이면(근무 중) 
  --AND A.RETIRE_DT = '20061031'  
  AND B.SCHOOLOUT_DT IS NOT NULL      --졸업만 해당  
GROUP BY A.COMPANY_CD, A.POST_CD, A.EMPLOYEE_ID, A.EMPLOYEE_NM, A.DUTY, A.POSITION, A.EMAIL, A.MOBILE, A.RETIRE_DT 
ORDER BY A.POST_CD ASC, 직책 DESC, A.EMPLOYEE_NM ASC  
