- WIPI 정의 
WIPI(Wireless Internet Platform for Interoperability, 무선인터넷 플랫폼 표준 규격)의 약자로 무선인터넷 시장의  
표준화의 필요성이 대두되면서 2001년 7월 부터 그 개발이 추진되었고 2003년 5월 플랫폼의 규격이 만들어 졌다. 
SKT 는 SK-VM  
KTF 는 브루 
LGT 는 J2ME 기반 
따라서 이 따로따로인 플랫폼을 통합하기 위해 정통부에서 주관하여 만든 통합 플랫폼이 WIPI 입니다. 
장점은 C, Java 모두 사용이 가능하다는 것이지요. 
다만 아직 보급이 덜 되었고, 이를 지원하는 단말기 역시 상대적으로 적지요. 
무엇보다 SKT에서 위피를 이용해 만든 프로그램을 위피 휴대폰에 탑재하기 위해서는 CP자격을 갖고 있어야  
하지만 일반인에겐 이 자격을 주지 않아 폰에 탑재하기 위해 아직 많은 사람들이 MIDP 로 전환합니다. 
KTF는 제도가 바뀌어 일반 개발자도 테스트용 폰을 등록해 거기에 탑재할 수 있지만 용량은 250kb로 제한됩니다. 

현재 위피를 모바일 표준으로 하기 위해 많은 노력을 기울이고 있으며  
브루의 모태인 퀄컴에서는 브루와 위피를 같이 사용하기로 합의를 본 것으로 알고 있습니다.  
하지만 SUN 사에 일정 로열티를 지불해야 하는 문제점을 안게 되었다는데 약간 퇴색되었다고 볼 수 있습니다. 

여기까지가 제가 위피로 졸작을 구현하면서 알게 된 정보이며 자세한 사항은  
http://developer.wipi.or.kr 
