오라클은 두 개의 모드로 움직일수 있다 
아카이브 모드를 운용하는 경우는 지금의 경우와 같이 특정 타임까지  
데이터를 복구할 수 있기에 아카이브 모드로 운용을 하지 
아카이브 모드로 운용되더라도. 데이터 파일은 백업을 반드시 받아야 하고 
그 데이터파일을 백업 받아놓은 시점부터 변경된 DB정보를 아카이브로그 파일로 보완하는 경우다. 
DB를 복구하려면 전체 백업받은 데이터 파일이 있어야 하고 그 시점 이후의 아카이블 로그 파일도 다 있어야 한다. 
만약 아카이브로그파일 중간에 빠지면 그 이후로는 복구가 안된다. 
---------------------------------------------------------------------------------------------------------------------- 

데이터파일을 백업받는 방법으로 네가 온라인 백업이라 얘기했던 핫백업과 DB를 완전히 내리고 받는 콜드백업이 있다. 
1. 핫백업의 경우 
DB를 운용중에 받아야 하므로 db파일을 그래도 copy 하는 방법은 안되고 
파일을 copy하기 전에 db에 현재 백업받으려는 파일이 뭐다라는 식으로 인식시켜주고 백업받고  
다시 끝나면 다시 db에 작업이 끝낫음을 알려주어야 한다. 

2. 핫백업의 아닌 경우 
너의 경우는 특정시점으로 복구하기 위한 절차상이므로 핫백업 시간만 만이 걸리지 의미가 없을듯하다. 
그냥 백업시 DB를 내리고 전체 데이터 파일을 copy하고 복구 해라. 
---------------------------------------------------------------------------------------------------------------------- 

내가 말하는 것은 복구를 위해서는 그전 백업받아놓은 데이터파일이 있어야 한다는 뜻이다. 
데이터파일에는 특정 시점의 시퀀스 정보가 있으므로. 
