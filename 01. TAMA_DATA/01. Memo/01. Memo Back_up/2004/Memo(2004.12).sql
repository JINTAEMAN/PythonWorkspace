-- 주문 후 중복 배송건 조회(거래처별 주문상세 숫자와 배송(T_STOREOUT) 숫자가 일치하지 않은것) @@@@@@@ =========> 
SELECT A.ORDER_DT, A.ORDER_NO, B.ENTR_NO, B.CNT AS 주문상세_수, C.CNT AS 배송_수 
FROM T_ORDER A, 
     (SELECT COMPANY_CD, MERCHANT_CD, ORDER_DT, ORDER_NO, ENTR_NO, COUNT(*) CNT 
      FROM T_ORDER_DTL 
      GROUP BY COMPANY_CD, MERCHANT_CD, ORDER_DT, ORDER_NO, ENTR_NO ) B, 
     (SELECT COMPANY_CD, RECV_MERCHANT_CD, RECV_DT, RECV_NO, COUNT(*) CNT 
      FROM T_STOREOUT 
      GROUP BY COMPANY_CD, RECV_MERCHANT_CD, RECV_DT, RECV_NO ) C 
WHERE A.COMPANY_CD = '01' 
   AND A.MERCHANT_CD = '0001' 
   AND A.ORDER_DT >= '20041101' 
   AND A.ORDER_TP ='01'         -- 주문유형(01:정상주문, 03:반품주문) 
   AND A.ORDER_STS >= '03'      -- 주문상태(03:결재완료) 
   AND B.CNT <> C.CNT           --거래처별 주문상세(T_ORDER_DTL) 숫자와 배송(T_STOREOUT) 숫자가 일치하지 않은것 
   AND B.COMPANY_CD = A.COMPANY_CD 
   AND B.MERCHANT_CD = A.MERCHANT_CD 
   AND B.ORDER_DT = A.ORDER_DT 
   AND B.ORDER_NO = A.ORDER_NO 
   AND C.COMPANY_CD = A.COMPANY_CD 
   AND C.RECV_MERCHANT_CD = A.MERCHANT_CD 
   AND C.RECV_DT = A.ORDER_DT 
   AND C.RECV_NO = A.ORDER_NO 
; 
