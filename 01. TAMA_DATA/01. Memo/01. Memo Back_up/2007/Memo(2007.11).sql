CREATE OR REPLACE FUNCTION FPwdDecode(INSTR VARCHAR2, keyValue VARCHAR2) 
RETURN VARCHAR2 
IS 
 enStr  VARCHAR2(30); 
BEGIN 
 dbms_obfuscation_toolkit.DESDecrypt( 
   input_string =>INSTR, 
                 key_string => RPAD(keyValue,8,'@'), 
                 decrypted_string => enStr); 
 RETURN trim(SUBSTR(enStr,1,30)); 
END; 
