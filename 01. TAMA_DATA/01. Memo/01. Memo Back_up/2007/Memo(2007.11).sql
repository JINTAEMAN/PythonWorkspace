CREATE OR REPLACE FUNCTION FPwdEncode(INSTR VARCHAR2, keyValue VARCHAR2) 
RETURN VARCHAR2 
IS 
 enStr  VARCHAR2(30); 
 inStr2 VARCHAR2(30); 

BEGIN 
 SELECT RPAD(SUBSTR(trim(LOWER(INSTR)),1,24),24,' ') INTO inStr2 FROM dual; 
 dbms_obfuscation_toolkit.DESEncrypt( 
   input_string => inStr2, 
                 key_string => RPAD(keyValue,8,'@'), 
                 encrypted_string => enStr); 
 RETURN (enStr); 
END; 
/  
