- 소방관련 용어 
GIS(Geographic Information System) 
GPS(Geographic Posting System) 
CTI(Computer Telephony Integraton)  
IEMS(Integration Emergency Management System): 통합재난관제시스템 
SMS(System Management Server) 서버: 시스템 관리 서버 
NMS(Network Management Server) 서버: 네트워크관리시스템 서버  
EMS(Elementary Management System)서버: 특정 감시대상 공간 내에 설치된 환경감시센서로부터 장애,  
경보 등의 제반 이력과 감시 데이터를 TCP/IP 혹은 모뎀 라인을 통해 서버로 전송 받아서 실시간으로 저장하고,  
저장된 데이터를 체계적으로 가공하여 사용자에게 제공하는 서비스 
ESM(Enterprise Security Management): 통합보안 관제 센터  
UMS(Unified Messaging System): ARS, E-Mail, FAX, SMS 등을 통합하여 관리, 운영할 수 있는 시스템 
MDT(Mobile Data Terminal): 무선 데이터 단말기 
ANI(Automatic Number Identificatio): 발신자 자동 식별(걸려 온 전화의 발신자 번호를 자동적으로 통지하고,  
데이터베이스 검색으로 상대의 성명, 소속이나 인상 등을 알 수 있는 기능) 
ALI(Automatic Location Identificatio): 자동 위치 추적 시스템 
AVL(Automatic Vehicle Location): 이동중인 차량의 위치를 GPS시스템 장비를 이용하여 파악한 후 이동 차량의  
위치를 실시간으로 무선 TRS망을 통하여 중앙관제소서 한눈에 전차량의 위치를 파악 할수 있는 시스템  
FRID(Fadio Frequency IDentification): 사물에 전자태그를 부착하고 무선통신 기술을 이용하여 정보 및 주변 
상황정보를 감지하는 인식기술. 
USN(Ubiquitous Sensor Network): 전자태크(RFID)와 U-센서를 BcN과 연계하여 사물의 정보를 인식,관리 
하는 네트워크. 
BCN(Broadband Convergence Network): 음성ㆍ데이터, 유무선 등 통신ㆍ방송ㆍ인터넷이 융합된 품질 보장형  
광대역 멀티미디어 서비스를 언제 어디서나 끊김 없이(Seamleass) 안전하게 이용할 수 있는 차세대 통합 네트워크.   
ITS(Intelligent Transportation Systems): 지능형 교통 시스템. 전자, 정보, 통신, 제어 등의 기술을 교통체계에  
접목시킨 지능형 교통 시스템이다. 신속, 안전, 쾌적한 차세대 교통체계를 만드는데 목적을 두고 있다. 
CFAMS(Computerized Fire Alarm Monitoring System): 개별 건물/상가/공자의 화재 경보 상태를 모니터링하고 
비정상적인 상태가 발생할 경우 다이얼 호출식의 전화선(PSTN)을 통해 관제센터로 그 내용을 전송할 모니터링 
시스템의 구축하는 것. 
DAC(Digital Alarm Communicator) 
텔레매틱스(Telematics): 차량의 인공위성을 이용한 GPS와 차량 내 무선 정보단말을 통해 차량과 운전자에게 긴급구조,  
교통정보 및 주행안내 등 다양한 정보 제공. 
BIS(Bus Information System): 버스정보안내 시스템 
BMS(Bus Management System): 버스운행정보 시스템 
FTTH(Fiber-To-The Home)전화국으로부터 각 가정까지 개별적으로 광섬유를 부설하여 광대역 서비스를 하는  
가입자망 방식. 전화, 팩스, 데이터, 텔레비전 영상의 광대역 서비스에 이르는 모든 통신 서비스가 한 줄의 광섬유로  
이루어질 수 있다. 그러나 파이버 투 더 홈(FTTH)은 각 가정마다 광섬유를 부설하고, 광망 종단 장치(ONU)도 개별적 
으로 설치해야 하는 등 경제성에 문제가 있다. 따라서 이미 사용 중인 광대역 방식을 활용하는 파이버 투 더 존(FTTZ)  
방식이 제안되었다. 
DIP(Daegu Institute of Digital Promotion): 대구디지털산업진흥원 
c.f) DIP(Dangerous Integration P): RFID·모바일 임베디드SW 기반 위험관제시스템 
