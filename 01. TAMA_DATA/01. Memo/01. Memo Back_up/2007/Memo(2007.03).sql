- 소프트웨어 공학 자격증 
CMMI(Capability Maturity Model Integration):  
1991년 미 국방부의 요청에 의해 미국 카네기멜론 대학의 소프트웨어 공학연구소(SEI,Software Engineering Institute)가  
개발한 S/W 개발 프로세스 성숙도 평가모델. 
1 ~ 5 레벨로 나뉘어져 있으며 각 레벨에 해당하는 품질수준을 나타냄 
CMM을 통해서 정확한 프로세스 수준을 확인할 수 있으며 지속적인 프로세스 개선을 할 수 있음 
CMM은 SEI에서 2005년 까지만 지원 할 예정이며 이 후는 CMMI로 통합함 
☞ 미 정부기관 SW 개발입찰의 경우 CMM 레벨3 이상의 업체만 입찰가능(CMM/CMMI 자체가 defacto 표준이 되어버림) 
CISA(Certified Information Systems Auditor): 정보시스템 감사 컨트롤 협회(ISACA)가 인증하는, 시스템을 독자적으로  
점검 ·평가할 수 있는 국제 자격이다. 
PMP(Project Management Professional): 프로젝트관리 전문가. 프로젝트관리 분야의 세계 최고 자격증으로 미국 PM  
전문기관인 PMI(Project Management Institute)가 1984년부터 PM의 전문성 확보와 체계적인 PM 기법을 체득시키기  
위해 시행하고 있는 자격증. 
ITSM(Information Technology Service Management): 정보 시스템 사용자가 만족할 수 있는 서비스를 제공하고  
지속적인 관리를 통해 서비스의 품질을 유지, 증진시키기 위한 일련의 활동, 즉 기업 내부의 기존 정보 통신(IT)  
관리 역할을 서비스 관점으로 바꿔 고객 중심의 IT 서비스를 관리하는 기법.  
ITIL(IT Infrastructure Library): 영국에서 태동한 정보 기술 IT 서비스를 지원 구축 관리하는 프레임워크 효과적인 IT  
서비스 관리를 위한 일종의 교본으로 ITIL에서 제시하는 서비스 관리 수준 SLA 프로세스는 사전 준비 SLA 개발 운영  
및 개선 단계의 생명 주기를 거치며 반복된다. 
