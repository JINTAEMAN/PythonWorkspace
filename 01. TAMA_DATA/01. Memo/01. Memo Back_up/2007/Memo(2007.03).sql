- 용어 설명 
CBD(Component Based Design): 객체지향 CBD 개발 방법론으로 architecture가 component를 지원하게 되면서  
부터 아마 등장한 것 같은데 특히 J2EE가 널리 사용되면서, 인터페이스가 동일하면 언제든지 컴포넌트를 수정해서  
올렸다 내렸다 할 수 있는 시스템들이 등장하면서 주목받고 있는 방법입니다. 
SOA(Service-Oriented Architecture): 기업의 정보 시스템을 공유와 재사용이 가능한 서비스와 컴포넌트 중심으로  
묶는 정보 기술 아키텍처. 기업의 소프트웨어 인프라를 구축하는 방법을 정의하는 것으로, 시스템을 누구나 이용  
가능한 서비스로 간주하고 연동과 통합을 전제로 아키텍처를 구축해 나간다. 단순 객체 접근 프로토콜(SOAP) 기반의  
웹 서비스가 보편적인 SOA로 서로 다른 이용자들이 서로 다른 방식으로 서비스와 의사 소통을 하면서도 통합 관리되는  
서비스들을 사용할 수 있다.  
UML(Unified Modeling Language): 소프트웨어 개발 과정에서 산출되는 산출물들을 명시, 개발, 문서화하기 위한  
모델링 언어이다. 여러가지 다이어그램들을 제시함으로써 소프트웨어 개발과정의 산출물들을 비주얼하게 제공하고,  
개발자들과 고객 또는 개발자들 간의 의사소통을 원활하게 할 수 있도록 하고 있다. 
