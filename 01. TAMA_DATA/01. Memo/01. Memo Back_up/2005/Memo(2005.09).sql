- ORA-02085 에러 문제  @@@@@@@ 
init.ora파일에서 global_names Parameter를 false로 하니깐 되네요.. 
alter system set global_names = FALSE 
------------------------------------------------------------------------------------------------------------------------ 

아마도 현재의 문제는 global_name 이 true 로 되 있어서 문제가 발생한거 같군요.... 
오라클 버전 별로 약간씩 방법이 틀리지만 
하나의 사이트에서 사용하시려면 global name 을 동일하게 
운영하시는 것이 좋습니다. 그리고 default name 도 가능하면 
동일하게 운영하시는 것이 좋구요. 
 global name 이 true 로 되 있으면 데이터 베이스 링크를 걸때  
 제가 알기로는 하나 밖에 인식하지 못하게 되는 것으로 알고 있습니다. 
 즉 global_name 이 false 로 하면 문제는 되지 않겠죠... 

만약 데이터 베이스 링크를 public 로 생성하지 않으시고 각각의 schema 별로 생성하신다면  
global_name 이 true 라면 하나 밖에 인식하지 못할 것이고 추가적으로 생성된 데이터 베이스 링크는 인식하지 못합니다.  
global_name 은 false 로 하시면 문제는 없습니다.  

그리고 가능하면 저 같은 경우는 데이터 베이스 링크 구성시 public 하게 구성합니다.  
그래야 데이터 베이스 링크를 사용하는데 관리가 편해 지거든요...  
