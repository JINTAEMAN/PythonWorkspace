-- L&B매장 분석 정보 조회 @@@@@@@@@@@@@  
SELECT A.MEM_NM, A.REG_ID, MAX(A.MEM_GRADE), 
SUM(A.AMT) AS 주문금액 
FROM T_ORDER A, T_ORDER_DTL B 
WHERE A.COMPANY_CD = B.COMPANY_CD 
    AND A.MERCHANT_CD = B.MERCHANT_CD 
    AND A.ORDER_DT = B.ORDER_DT 
    AND A.ORDER_NO = B.ORDER_NO 
    AND A.COMPANY_CD = '01' 
    AND A.MERCHANT_CD = '0001' 
    AND A.ORDER_TP IN ('01')         --주문유형(01:정상주문, 03:반품주문) 
AND A.ORDER_STS >= '03'    --주문상태(03:결제완료) 
AND A.ORDER_DT >= '20050101' 
AND SUBSTR(A.ORDER_DT,1,6) = TO_CHAR(TO_DATE(SYSDATE),'YYYYMM') -1  --현재 월    
--AND B.DISPLAY_STORE_CD = 'S065'  --전시상점코드(S065:노블레스매장) 
--AND B.CATEGORY1_CD IN ('U02')    --대카테고리코드(U01:비즈메카매장, U02:킬러앱매장) 
AND A.MEM_GRADE = '99'   --직급(99:무료회원) 
GROUP BY A.MEM_NM, A.REG_ID 
ORDER BY A.MEM_NM 
--; 
