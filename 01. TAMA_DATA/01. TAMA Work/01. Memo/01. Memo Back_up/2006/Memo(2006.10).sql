--BC카드 주문 정보 조회 @@@@@@@@@ 
SELECT A.REG_ID AS 회원ID, A.MEM_NM AS 회원명, MAX(MEM_PHONE) AS 회원전화번호, MAX(MEM_EMAIL) AS 회원EMAIL, 
   NVL(SUM(B.REAL_SALE_PRICE),0) AS 주문금액 
FROM T_ORDER A, T_ORDER_DTL B, T_AR C 
WHERE A.COMPANY_CD = B.COMPANY_CD 
AND A.MERCHANT_CD = B.MERCHANT_CD 
AND A.ORDER_DT = B.ORDER_DT 
AND A.ORDER_NO = B.ORDER_NO 
AND A.COMPANY_CD = C.COMPANY_CD 
AND A.MERCHANT_CD = C.MERCHANT_CD 
AND A.ORDER_DT = C.ORDER_DT 
AND A.ORDER_NO = C.ORDER_NO 
AND A.COMPANY_CD = '01' 
AND A.MERCHANT_CD = '0001' 
AND A.ORDER_TP IN ('01') --주문유형(01:정상주문, 03:반품주문) 
AND A.ORDER_STS > '02' --주문상태 
AND A.ORDER_DT >= '20061009' --주문일자(이벤트 일자) 
AND A.ORDER_DT <= '20061107'  
AND A.ORDER_DT <= TO_CHAR(TO_DATE(SYSDATE),'YYYYMMDD')     --주문일자(오늘) 
AND (SELECT CARD_NM FROM T_CARD_BIN WHERE CARD_BIN LIKE SUBSTR(C.CARD_NO,0, 6)) = 'BCcard' 
AND A.AR_WAY_CD IN ('02', '03')  --결제방법코드(01:현금, 02:카드, 03:현금/카드, 04:R&F머니) 
AND C.AR_WAY_CD IN('20')  --시제유형코드(10:현금, 20:카드, 30:포인트, 40:쿠폰) 
AND C.AR_STS IN ('21')       --결제상태(21:카드승인)    
--AND A.REG_ID = 'hi51' --등록자ID  
AND B.DISPLAY_STORE_CD NOT IN('S013')   --매장코드(R&F매장이 아닌 것) 
AND NOT (B.DISPLAY_STORE_CD = 'S063' --하이이머니 매장이 아닌 것   
AND B.CATEGORY_CD IN ('W51010101', 'W51010102', 'W51010103','W51020101', 'W51020201', 'W51020301', 'W51020401')) 
GROUP BY A.REG_ID, A.MEM_NM 
ORDER BY SUM(B.REAL_SALE_PRICE) DESC  
--;  
