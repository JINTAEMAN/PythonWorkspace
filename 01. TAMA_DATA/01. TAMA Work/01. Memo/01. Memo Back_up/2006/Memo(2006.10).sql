-- 디파트별 전시상품 조회(상품검색 조회)  @@@@@@@@  ---> 쇼핑몰에 전시된 상품  
SELECT A.DEPART1_CD AS DEPART1, E.CATEGORY1_NM, A.DEPART2_CD AS DEPART2, E.CATEGORY2_NM,  
	   A.DEPART3_CD AS DEPART3, E.CATEGORY3_NM, 
	   C.CATEGORY_CD, A.ITEM_CD AS 상품코드, A.ITEM_NM AS 상품명, E.END_DT 
-- SELECT COUNT(*) 
FROM T_ITEM A, T_MERCHANT_ITEM B, T_CATEGORY_ITEM C, T_ITEM_WEB D, T_CATEGORY E 
WHERE B.COMPANY_CD = A.COMPANY_CD 
AND B.ITEM_CD = A.ITEM_CD 
AND B.COMPANY_CD = C.COMPANY_CD 
AND B.ITEM_CD = C.ITEM_CD         
AND B.COMPANY_CD = D.COMPANY_CD(+)         
AND B.ITEM_CD = D.ITEM_CD(+) 
AND B.COMPANY_CD = E.COMPANY_CD         
AND C.STORE_CD = E.STORE_CD 
AND C.CATEGORY_CD = E.CATEGORY_CD    
AND B.COMPANY_CD = '01' 
AND B.MERCHANT_CD = '0001' 
AND B.SALE_STS_CL IN('01','02')  --판매상태구분(01:ACTIVE, 02:PENDING, 03:INACTIVE, 05:DELETE) 
AND A.DEPART1_CD IN ('510', '520', '530', '610')   --DEPART1코드 
--AND A.ITEM_CD = '00165126'      --상품코드 
AND A.USE_YN = 'Y'  --사용여부  
AND E.USE_YN = 'Y'  --사용여부  
AND (E.END_DT IS NULL OR E.END_DT >= TO_CHAR(TO_DATE(SYSDATE),'YYYYMMDD')) --완료일자 
ORDER BY A.DEPART1_CD, A.DEPART2_CD, A.DEPART3_CD, A.ITEM_NM 
--; 
