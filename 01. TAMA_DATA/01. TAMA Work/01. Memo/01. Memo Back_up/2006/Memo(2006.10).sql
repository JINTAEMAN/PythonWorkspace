RETURN NUMBER 
IS 
  v_NUM      PLS_INTEGER  DEFAULT 0; 
BEGIN   

  SELECT TO_NUMBER(param_STR) INTO v_NUM FROM DUAL; 
  RETURN 1; 

  EXCEPTION 
    WHEN INVALID_NUMBER THEN  --ORA-01722 
      RETURN 0; 
     
    WHEN OTHERS THEN 
      DBMS_OUTPUT.PUT_LINE('param_STR : '  param_STR  ' : '   
                           TO_CHAR(SQLCODE)  ':'  SQLERRM); 
      RETURN 0; 

END UDF_ISNUM; 
