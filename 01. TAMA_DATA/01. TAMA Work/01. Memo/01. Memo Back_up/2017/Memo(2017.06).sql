/*****************************************************************************************
* Function명 : getCurTime
* 설명       : 현재 시간으로 반환하는 함수 
* Params     :    1. dt : Fri Jun 30 2017 10:36:32 GMT+0900 (대한민국 표준시)
			2. fmt :  "Y-m-d"(년, 월, 일),  "H:i:s"(시,  분, 초) ==> 변경하고 싶은 날짜 타입
			==> 예) var strTodayTime = this.fromTimetoFormatString("Fri Jun 30 2017 10:36:32 GMT+0900", "H:i:s"); 	// 시,  분, 초
* Return     :
* 작  성 자 : 진태만
* 작  성 일 : 2017.06,.30
*****************************************************************************************/  
LIB_DATE.fromTimetoFormatString = function(dt, fmt) 
{ 
	return fmt.replace(/(Y|y|m|d|h|H|i|s|am|pm)/gi, 
	
	function($1){ 
		switch ($1)
		{ 
			case 'Y': return dt.getFullYear();   	// 년
			case 'y': return dt.getFullYear().toString().substr(2); 	
			case 'm': return dt.getMonth() + 1; 	 // 월
			case 'd': return dt.getDate(); 			 // 일년
			case 'h': return (h = dt.getHours() % 12) ? h : 12;  // 시
			case 'H': return dt.getHours(); 
			case 'i': return dt.getMinutes(); 	 // 분
			case 's': return dt.getSeconds(); 	 // 초
			case 'am': return dt.getHours() < 12 ? 'am' : 'pm'; 
			case 'pm': return dt.getHours() < 12 ? 'am' : 'pm'; 
			} 
		} 
	); 
} 
