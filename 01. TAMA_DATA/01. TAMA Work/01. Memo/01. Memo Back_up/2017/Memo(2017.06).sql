1) Skip All Breakpoints : 모든 브레이크 포인트 건너뜀
2) Resume(F8키) : 다음 브레이크포인트까지 진행함
3) Suspend : 쓰레드를 일시 정지하며 현재 수행문에 지정한 것과 같음
