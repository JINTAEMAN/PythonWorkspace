- 카드깡 @@@@@@@@@@@ 
카드한도가 100만원이라고 가정하면(계산편하게) 
현금은 못 빼지만(이미다쓴경우가 대부분), 상품은 살수 있다는 겁니다. 
이것을 이용해서 사채업자들이 상품을 살수있는 금액만큼  
가짜로 물건을 산것처럼 하여서 카드결제를 신청합니다. 
실제 물건을 구입하지는 않았지만 물건을 구입한것처럼 매출을 만들고 
그것을 그 업소에서는 매출전표를 카드회사에 청구한다. 
카드 주인에게는 일정수수료를 제외한 나머지 금액을 현금으로 주는 거죠. 
그렇게 되면 카드주인은 100만원을 카드사에 갚아야하고 
실제 받는 돈은 80~89만원정도를 받게 됩니다. 
----------------------------------------------------------------------------------------------------------

- 카드깡 
유령 허위 매출전표 발행(100만원)--> 카드 주인(즉시 80~85만원 받음)  
--> 카드깡 업자(카드사에 100만원 청구: 3-4일후에 입금) 
