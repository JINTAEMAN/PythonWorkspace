- 인터넷 카드깡 @@@@@@@@@@@ 
카드를 이용한 사채라는 점은 종전의 카드깡과 같으나, 인터넷 쇼핑몰이 카드 매출 전표상에 판매자로 되고,  
카드 대금도 수령하기 때문에, 카드깡 업자는 위장 가맹점이 필요없고, 허위의 매출전표를 작성할 필요도 없다.  
---------------------------------------------------------------------------------------------------------- 

우선 인터넷 쇼핑몰 운영자들이 보유한 고객 리스트를 통해 인터넷 카드깡의 의심이 있는 
거래에 대한 자체 감독을 강화해야 한다.  
동일한 시간에 물건을 다량 주문하거나, 이름이 서로 다른 고객의 휴대폰 번호가 같은 경우, 
카드를 발급 받고 처음 사는 물건이 상품권인 경우에는 쇼핑몰 운영자들은 일단 경계하여야 하는 것이다. 
