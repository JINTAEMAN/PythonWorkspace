     -- ENTR_NO = A.ENTR_NO AND         --거래처 코드 
     --SEQ_NO = A.SEQ_NO 
     --ITEM_CD = A.ITEM_CD 
  ) > 1 
ORDER BY A.MEIP_NO, ITEM_CD, A.MAGAM_DATE DESC 
