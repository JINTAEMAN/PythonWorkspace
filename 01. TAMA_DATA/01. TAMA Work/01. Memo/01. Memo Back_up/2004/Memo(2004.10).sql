MDAC 설치 프로그램이 작동하지만 MDAC가 올바로 설치되었는지 확인하는 방법이 있습니까?  
다음 문제 해결 테스트는 MDAC가 올바로 설치되었는지 확인하는 간단한 방법을 몇 가지 제공합니다.  
MDAC는 상호 종속적인 여러 DLL로 구성되어 있기 때문에 MDAC가 성공적으로 설치되었는지에 대한  
유일하고 진정한 테스트는 모든 구성 요소가 제대로 작동하는지 확인하는 응용 프로그램으로  
MDAC를 완벽하게 테스트하는 것입니다. 다음 테스트는 컴퓨터에 특정 응용 프로그램이 설치되어 있을 것을  
요구하지 않으므로 모든 시스템에서 MDAC 설치를 신속히 확인하는 데 유용합니다.  
먼저 ODBC 관리자가 작동하는지 확인합니다. 제어판을 열고 ODBC 데이터 원본 아이콘을 선택합니다.  
Windows 2000에서 제어판으로 이동하고 관리 도구를 선택하고 데이터 원본(ODBC)을 누릅니다.  
ODBC 데이터 원본 관리자라는 제목의 대화 상자가 나타납니다. 다음 단계를 통해 새 ODBC 데이터 원본을 만들어 봅니다.  
ODBC 관리자에서 시스템 DSN 탭을 선택합니다. 
추가를 눌러 새 ODBC 데이터 원본을 만듭니다. 
목록에서 Microsoft Access Driver(*.mdb)를 선택하고 마침을 누릅니다. 
데이터 원본 이름 입력란에 TEST를 입력합니다. 
만들기를 누릅니다. 
데이터베이스 이름 입력란에 C:\TEST.MDB를 입력하고 확인을 누릅니다.  
이제 데이터베이스가 성공적으로 만들어졌음을 나타내는 메시지 상자가 나타납니다. 확인을 누릅니다. 
확인을 다시 눌러 ODBC Microsoft Access 설정 대화 상자를 닫고 새 ODBC 데이터 원본을 만듭니다. 
그런 다음 확인을 눌러 ODBC 데이터 원본 관리자 대화 상자를 닫습니다. 
앞의 단계가 성공적으로 완료되면 ODBC 핵심 파일과 ODBC 관리자가 올바르게 설치됩니다.  
다음에 Microsoft 데이터 연결 파일을 만들고 다음 단계를 통해 앞에서 만든 TEST 데이터 원본에 대한 연결을 테스트합니다.  
컴퓨터의 바탕 화면을 마우스 오른쪽 단추로 누르고 새로 만들기 메뉴에서 Microsoft 데이터 연결을 선택합니다.  
이렇게 하면 바탕 화면에 "New Microsoft Data Link.udl"이라는 새 파일이 만들어집니다.  
Windows 2000에서 바탕 화면을 마우스 오른쪽 단추로 누르고 텍스트 파일을 만든 다음 Test.udl로 이름을 바꿉니다.  
이 파일을 두 번 눌러 데이터 연결 속성 대화 상자를 엽니다. 
데이터 원본 이름 사용 드롭 다운 상자에서 앞에서 만든 데이터 원본인 TEST를 선택합니다. 
연결 테스트 단추를 누릅니다. 이제 연결 테스트를 성공했습니다.라는 메시지 상자가 나타납니다. 
앞 단계가 성공적으로 완료되면 OLE DB와 ODBC는 물론 Access ODBC 드라이버의 테스트가 수행된 것입니다.  
