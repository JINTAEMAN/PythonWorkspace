SELECT A.MAGAM_DATE, A.MERCHANT_CD, A.MAGAM_GUBUN, A.MEIP_GUBUN, A.ENTR_NO, A.MEIP_NO, A.ITEM_CD, 
A.ITEM_NM, A.QTY, A.MEIP_DANGA, A.RECEIVER_NM, A.RECEIVER_ADDR 
FROM T_MAGAM_MEIP A 
WHERE A.COMPANY_CD = '01' AND 
  A.MERCHANT_CD = '0001' AND 
A.MAGAM_GUBUN ='02' AND     --매감구분(01-정상-출고완료/입고완료 02:반품-회수완료/입고반품완료) 
