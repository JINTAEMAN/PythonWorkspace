A.MAGAM_DATE >= '20040101' AND 
 ( 
   SELECT COUNT(*) FROM T_MAGAM_MEIP WHERE 
     COMPANY_CD = A.COMPANY_CD AND 
     MERCHANT_CD = A.MERCHANT_CD AND 
     MAGAM_GUBUN = A.MAGAM_GUBUN AND   --매감구분 
