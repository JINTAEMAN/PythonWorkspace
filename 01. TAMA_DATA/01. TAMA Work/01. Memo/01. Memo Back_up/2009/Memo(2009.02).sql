1. 복부비만의 예방과 대처
저녁과식, 야식을 피해야 한다. 
술자리를 줄여야 한다. 
지방질, 당분이 높은 음식을 줄인다. 지방과 당분은 복부에 살이 더 잘 찌게 한다. 
오래앉아 있는 걸 피한다. 오래앉아서 일 하는 사람일수록 복부지방이 더 잘 쌓인다. 
식이조절을 하고 유산소운동을 하면 자연스럽게 빠진다. 

2. 복부비만의 운동법 
복부는 원래 살이 잘 찌기도 하는 부위이지만 살이 빠지면 제일 먼저 빠지기도 하는 부위이기도 하다. 
빨리 걷기, 달리기, 수영, 자전거 같은 유산소운동만으로도 복부비만은 해소가 잘 되는 편이다. 
옆구리운동이나  복근스트레칭을 꾸준히 해주는 것도 복부비만의 해소에 도움이 된다. 
윗몸일으키기는 무산소운동이 되기 쉬워서 지방분해에는 큰 도움이 되지 않는다.
