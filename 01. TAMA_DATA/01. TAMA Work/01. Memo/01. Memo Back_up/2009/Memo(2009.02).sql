☆ 탄수화물 중독의 증거 윗배 없애기
A. 누워서 상체 올리기
윗몸 일으키기와 비슷한 동작이지만, 학창 시절 하던 윗몸 일으키기와는 전혀 다른 운동이다. 상 복부의 근육을 
단련시켜 보기 흉한 윗배의 군살을 제거하는 데 좋다.  

바닥에 누운 상태에서 무릎을 직각으로 세운다. 입으로 숨을 내쉬면서 상복부의 힘만으로 상체를 들어올린다. 
상체가 더 이상 올라갈 수 없는 지점에서 동작을 멈추고 버틴다. 숨을 모두 내쉬었을 때 천천히 처음 자세로 돌아온다. 
NG: 이 운동은 목과 어깨 근육에도 자극이 가기 때문에 목이 아프다. 그렇다고 손으로 머리를 받치거나 목을 
꺾으면 배로 가는 힘이 분산되니까 손은 관자놀이 옆에 대기만 한다. 

B. 다리 올리고 상체 들기 
의자를 이용하니까 언뜻 보기에는 ‘누워서 상체 올리기’보다 쉬워 보이지만, 더 어려울 수도. 배 앞쪽 복직근에 
힘이 들어가면서 근육을 강화시켜주는 효과가 있다.   
바닥에 누운 상태에서 무릎을 직각으로 세운다. 입으로 숨을 내쉬면서 상복부의 힘만으로 상체를 들어올린다. 
상체가 더 이상 올라갈 수 없는 지점에서 동작을 멈추고 버틴다. 숨을 모두 내쉬었을 때 천천히 처음 자세로 돌아온다. 
NG: 이 운동은 목과 어깨 근육에도 자극이 가기 때문에 목이 아프다. 그렇다고 손으로 머리를 받치거나 목을 
꺾으면 배로 가는 힘이 분산되니까 손은 관자놀이 옆에 대기만 한다. 

☆ 아랫배 없애기
C. L자로 골반 올리기 
아랫배에 힘을 주어 동작을 실시하면 아랫배 근육이 강화되는 효과가 있다.   
누운 자세에서 다리를 위로 곧게 펴 직각이 되도록 올린다. 양손으로 바닥을 밀면서 골반을 위로 밀어올린다. 
골반이 많이 올라가지는 않지만 배가 가장 당기는 부분에서 멈춘다. 그 다음 천천히 다리를 내리고, 위로 올렸다 
다시 내려오기를 반복. 느린 속도로 해야 효과가 좋다. 
NG: 머리를 들면 힘이 분산되어 아랫배 근육에 압박이 가지 않는다. 뱃살 빼기 효과 제로.

D. 두 다리 구부려 골반 올리기 
하복부를 이용해 골반을 들어올리는 운동. 처음에는 골반이 거의 들리지 않을 수도 있지만 꾸준히 해서 근력이 
발달하면 쉽게 올릴 수 있다.

바닥에 누운 상태에서 두 무릎을 붙이고 직각으로 세운다. 입으로 숨을 내쉬면서 하복부의 힘만으로 골반을 
들효과 1등 동작_활자세 상체와 하체가 동시에 강하게 자극됨으로써 아랫배 쪽의 군살을 없애줄 뿐 아니라 
목과 등, 허리를 강화시켜주면서 비만을 해소하는 동작. 
NG: 손바닥에 힘을 주거나 반동을 이용해 운동을 하면 뱃살을 빼는 데는 아무런 도움이 안 된다. 하복부의 힘을 
이용해 골반을 들어 올리는 것이 중요. 

☆ 술배에 효과보는 동작
E. 엎드려서 다리 올리기
두루뭉술한 남편의 뱃살 제거에는 엎드려 다리를 올리는 동작이 효과적. 배와 척추 기립근(등 하부)의 군살을 제거하고
허리를 강하게 해주는 일석이조의 효과가 있다. 
엎드린 상태에서 다리와 팔을 어깨 너비로 벌리고 최대한 펴준다. 목은 척추와 일직선으로 유지한다. 입으로 숨을 
내쉬면서 팔과 다리를 교차하며 올린다. 가능한 한 천천히 하는 것이 포인트. 다리가 더 이상 올라가지 않는 지점에서 
등 하부와 엉덩이, 허벅지의 자극을 느끼면서 버틴다. 코로 숨을 들이쉬며 천천히 처음 자세로 돌아오고, 발끝이 바닥에 
닿을 때 반대쪽 다리와 팔을 올린다. 
NG: 머리를 뒤로 꺾어 시선을 정면으로 향하면 팔과 다리 근육이 받는 자극이 줄어든다. 최대의 효과를 얻으려면 고개를 
숙이고, 시선은 바닥으로 향한다

F. 무릎 구부리고 상체 올리기 
무릎을 구부리고 상체를 들어올리는 복근 운동을 하면 배 앞부분에 있는 복직근에 힘이 들어가 근육이 단련된다. 
술 마셔서 나온 술배에 효과적. 
바닥에 누워 무릎을 구부려 가슴 앞으로 당긴다. 그 상태에서 양손을 머리 옆에 대고 상체를 일으킨다. 가슴 아래쪽 배 
근육이 가장 심하게 당길 때까지 몸을 일으키고, 견디기 힘들 때 천천히 바닥으로 내려간다. 
NG: 상체를 들어 올릴 때 팔꿈치가 무릎에 닿거나 깍지를 껴서 목이 꺾이지 않도록 한다. 
