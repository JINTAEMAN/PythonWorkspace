 다. 증권 용어 정리
뮤추얼펀드(Mutual Fund): 투자자가 펀드에 출자를 해 주주가 되며 펀드는 하나의 독립된 회사로 운영된다. 
뮤추얼펀드가 법률상 독립된 회사이기 때문에 기존 수익증권에 비해 주주의 운영 참여가 자유롭고 개방적인 
특징이 있으며 투명성도 높은 것으로 평가된다.
인덱스 펀드(index fund ): 시장의 장기적 성장추세에 맞춰 펀드의 수익률을 주가지수 수익률 변동과 연동시킴
으로써 주가지수 수익률만큼 펀드의 수익률을 실현시키고자 하는 것. 예컨대 고평가된 선물을 팔고 상대적으로 
저평가된 현물을 매입하는 경우 선물시장의 매매대상인 주가지수를 실제로 매수할 수 없기 때문에 지수에 비슷
하게 움직이는 종목들로 구성된 종목들(인덱스 바스켓)을 매매함으로써 동일한 효과를 볼 수 있다. 
상장지수펀드(Exchange Traded Fund): KOSPI200, KOSPI50과 같은 특정지수의 수익율을 얻을 수 있도록 설계된 
지수연동형 펀드(Index Fund). 인덱스 펀드와 뮤추얼 펀드의 특성을 결합한 상품이다. 2002년 처음으로 도입된 
ETF는 인덱스 펀드와는 달리 일반 주식처럼 자유롭게 사고 팔 수 있다.
전환증권(Convertible): 미리 정한 조건, 즉 특정한 시한내에 그 증권소지자의 의사에 따라 일정한 수의 보통주식
으로 전환할 수 있는 우선주(전환사채)를 지칭하는 말이다. 전환권이 행사될 경우 발행기업에 대하여 추가적인
자본의 유입이 수반되지 않고 사채 또는 우선주 지분이 자기자본으로 그 성격이 전환된다. 
우선주는(Preferred): 우선주는 보통주에 대해 배당이나 기업이 해산할 경우의 잔여재산의 분배 등에서 우선권을 
갖는 주식을 말한다.
채권(bond): 정부, 공공기관, 특수법인과 주식회사 형태를 갖춘 사적기업이 일반 대중 투자자들로부터 비교적 
장기의 자금을 조달하기 위해 발행하는 일종의 차용증서이다.
교체매매(SWAP): 포트폴리오에 포함돼 있는 자산을 매각해 그 자금으로 다른 자산을 구입, 포트폴리오를 재구성
하는 것을 말한다.
