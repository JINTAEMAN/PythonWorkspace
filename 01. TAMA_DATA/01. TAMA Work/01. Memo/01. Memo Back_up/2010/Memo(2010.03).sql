작업을 도와주는 역할 
2. 예를 들어 insert하기위해 dao에서 쿼리문을 모두 세팅해 excute시켜야 함 
3. Hibernate에서는 XXXX.save(dataTO) 이렇게 save라는 메소드를 이용하면 맵핑정보를 읽어 dataTO에 담겨있는  
데이터를 DB에 저장 
