- 조인 
1. 동등조인: '=' 로값을 비교하는것  
SELECT *  
WHERE A.STNO = B.STNO 
; 
2. 자연 조인: 동인 조인 결과에서 중복되는 속성을 제거한 조인 
SELECT A.*, B.COURSE, B.SCORE 
FROM A, B 
WHERE A.STNO = B.STNO  
; 
3. 외부 조인: 내부조인은 여러테이블간의 공통된 컬럼의 값이 동일한 조건들을 가져온다면, 공통컬럼값이 없더라도 해당  
로우들이 조회결과에 표시되게 해준다. 
SELECT *  
FROM A LEFT OUTER JOIN B 
ON A.STNO = B.STNO 
;  
SELECT *  
WHERE A.STNO = B.STNO(+) 
; 
