MERGE INTO STD_STOCK_MASTER SSM
USING 
	  (SELECT SUM(XOI.ONHAND_QTY) AS SUM_QTY, XOI.DWG_NO 
	   FROM XINV_ONHAND_FOR_STOCK_V$ERP XOI
	   WHERE 1=1
	   GROUP BY DWG_NO
	   ) A1
	ON   (
		   A1.DWG_NO = SSM.NO_DWG
		   )
WHEN MATCHED THEN
	UPDATE SET SSM.QTY_ONHAND = A1.SUM_QTY
