/* Stock 품목등록 정보 조회 ==> [selectStd000050.STD000050_mapper.xml] [2019.02.27, by 진태만] */ 
SELECT   COUNT(V.I_SPEED) OVER () CNT  -- 조회 건수 
			, V.INQ_STOCK_YN             -- 견적예약  
			, V.OOL_STOCK_YN           -- 수주예약  
			, V.QTY_MAX           			-- Max 재고 
			, TO_NUMBER(NVL(V.QTY_MAX, 1)) * 0.1 AS QTY_ORDER_BAS		-- 발주 기준재고 = MAX 재고 * 발주 기준재고 비율
			, (V.ONHAND_QTY - V.OOL_STOCK_YN) AS QTY_AVAILABLE   	   -- 가용재고 = 재고 - 수주예약
			, (V.QTY_AVAILABLE - V.QTY_MAX) AS QTY_STOCK_SHORT		   	-- 재고부족 = 가용재고 - 발주기준재고
FROM STD_STOCK_MASTER_V V 			-- Stock 품목등록 정보 View
WHERE 1=1   
 1) Stock 품목 칼럼 저장 ==> Max 재고, L/T, 구매수량, 구매예산(단가), 선급, MOQ 
 2) Stock 품목 상세 칼럼 저장 ==> L/T, 구매수량, 구매예산(단가), 선급
