- java의 for 문은 두 가지 형태가 존재 
 1] 첫번째 for 문 
C언어나 C++등의 for 문과 같은 구조를 가진 첫번째 for 문 입니다.
public class Test {  
	public static void main(String[] args)
	{ 
		for(int i=0, i < 100; i++)
		{
			System.out.println("[i]"+ i); 		 
		}
	}	
}

[2] 두번째 for 문
C#의 foreach문과 유사한 java의 for 문입니다. 
배열에 이용되는 for 문입니다. for(변수 : 배열) 이렇게 구성됩니다. 
public class Test {  
	public static void main(String[] args)
	{
		int arr[] = {1, 2, 3, 4, 5};
		
		for(int num : arr)
		{
			System.out.println("[num]"+ num); 		 
		}
	}	
}

이 코드의 의미는'arr' 배열에 들어 있는 값들을 하나씩 'num' 변수에 대입 시킵니다.
반복문이 돌 때마다 'num' 에 들어 있는 값이 변하게 됩니다. 
처음에는 1, 다음에는 2, 3, 4, 5 순서대로 하나씩 'num' 에 대입됩니다. 
총 5번을 반복하며 'num' 의 값을 출력합니다.

결과는 1부터 5까지 출력합니다.
출처: https://01027921288.tistory.com/entry/JAVA두가지-for-반복문?category=361584 [크히히] 
