기능을 어느 제품이 가장 많이 충족시킬 수 있을까?” 하는 것이다. 그러나 그럼에도 불구하고 기본적으로  
내장되어 있어야 하는 기능은 있다. 아래 기능은 X Internet 제품의 필수 기능이며 체크 사항이다 

