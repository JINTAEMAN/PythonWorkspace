- 퇴직연금제 
퇴직연금제는 연봉액에 퇴직금이 포함되어 있다고 생각이 됩니다. 
기존의 퇴직금 제도 같은 경우, 한해 170만원 정도가 적립되는 것입니다. 1년 이상 근무자가 마지막으로 근무한  
해의 기본급을 기준으로 계산한다고 알고 있습니다. 즉, 님의 연봉은 2000만 원 정도로 계산되는데, 이를 13으로  
나누어 12에 해당하는 것을 매달 나누어 지급하고 나머지 1은 퇴직금으로 적립하는 것입니다. 
다만, 연금의 경우 55세 이상으로서 퇴직연금 가입기간이 10년 이상인 가입자에게 지급됩니다. 이 경우 연금은 
5년 이상 지급하되 노사가 합의를 통하여 10년, 종신 등 연금 지급기간을 정할 수 있습니다. 
일시금은 연금수급요건을 갖추지 못하였거나 일시금 수급을 원하는 가입자에게 지급됩니다. 

새로 도입된 퇴직연금제도는 크게 확정기여형(DC)과 확정급여형(DB)이 있는데, 현재 대부분의 중소기업은  
DC형을 선택한 것으로 알고 있습니다. DC형은 1년 중 한달 분을 적립하되, 운용실적에 따라 퇴직금을 받게  
되는 방식입니다. 반면 DB형은 받을 퇴직금은 정해져 있고, 운용실적에 따라 내는 비용이 달라집니다.  
그래서 DC형은 근로자가 DB형은 회사가 운용의 주체가 됩니다. 
