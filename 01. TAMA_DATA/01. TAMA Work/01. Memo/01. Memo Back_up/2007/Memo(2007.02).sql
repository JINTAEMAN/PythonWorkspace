X 인터넷: X Internet이 인터넷과 C/S의 장점을 모두 수용한 것이라고 하였다. 그 
렇다면 X Internet은 인터넷인가? 아니면 C/S인가? X Internet은 결국 인터넷이다.  
아키텍쳐는 우리가 흔히 구축해온 인터넷과 동일하다. 단,사용자 인터페이스(UI)만 C/S를 따른다. 
, X Internet은 사용자 PC, 노트북, PDA, WebPad 등에 지역적으로 인스톨되어 기능적인 면에서 C/S처럼  
풍부함을 제공하고 한 곳에 집중된 관리를 함으로써 전개(Deploy)를 용이하게 하는 인터넷의 잇점이  
융합된 응용프로그램의 다음 세대이다. 물론 지역적으로 인스톨되는 파일의 사이즈는 한번만 다운로드  
받아 활용할 수 있는 매우 작은 크기이다.  

X Internet은 필요한 정보만을 혹은 바뀐 정보만을 서버와 주고 받는다. 과거 C/S를 사용할 경우 부서  
선택을 바꾸었다고 하위 부서를 디스플레이 해주는 화면이 Reload되지 않는 것처럼 필요한 정보만을  
주고 받는다. 즉, 화면을 디스플레이 해주기 위한 태그 정보는 재송수신할 필요가 없다.  
즉, 인터넷과 대비해서 소수의 비트만 교환되고 있다. 

현시점을 기준으로 executable Internet을 벗어나 그나마  
extended Internet을 지원하고 있는 제품은 투비소프트의 MiPlatform 정도이다. 
X internet는 기업을 어떻게 변화시킬 것인가? 
X Internet은 3가지 면에서 기업을 변화시킬 것이다. 
1. 응용프로그램이 구현되는 방법 
2. 사용자가 웹을 사용하는 방법 
3. 회사가 비즈니스를 영위하는 방법 

