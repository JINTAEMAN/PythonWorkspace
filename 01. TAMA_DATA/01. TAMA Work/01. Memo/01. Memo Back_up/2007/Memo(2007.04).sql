- 제안서 분석
- 기획서 작성 요점 
(1) 기본 작성법 
 가. 간결한 기획서 작성을 목표로 한다. 
 나. 간결하고 이해하기 쉬운 기획서를 작성한다. 
 다. 논리가 중요하다. 
(2) 기획 의뢰자. 진행 방법 
 가. 기획 의뢰자의 입장을 고려한다.  
 나. 제안 상대의 입장에 서서 기획서를 작성한다. 
 다. 사내를 대상으로 한 자극적인 기획서는 용기가 필요하다. 
 라. 사내를 대상으로 하는 기획서는 사전 조율이 필요하다. 
 마. 사전 협의를 잘 실행한다. 
 바. 원활하게 진행하는 요령은 한 단계 한 단계 확인하는 것이다. 
(3) 구성 
 가. 기획서는 [배경], [목적], [전략], [실시 계획]의 4가지로 구성된다.  
 나. [전략]은 기획의 요점이다.  
 다. 중요도가 높은 순으로 서술한다. 
 라. 소제목과 내용을 일치시킨다.  
 마. 구체적으로 작성한다. 
 바. 필요한 것에 중점을 둔 기획서를 작성한다. 
(4) 내용 
 가. 간결하게, 그렇지만 전략은 심도 있게 작성한다.  
 나. 기획의 포인트를 깊이 있게 파고들자.   
 다. 개성있는 기획서 작성이 중요하다. 
 라. 색다른 아이디어를 추가하면 기획서가 흥미로워진다. 
 마. 여러가지 아이디어를 추가하면 기획서의  깊이가 더해진다.  
 바. 독특한 기획을 실현하기 위해서는 확실한 증명이 필요하다. 
 사. 평범한 기획을 우습게 보지 마라. 
 아. 실시 계획은 엄밀하게 검토한다. 
 자. 캠페인은 컨셉이 중요하다. 
 차. 경쟁사와의 차별화를 도모한다. 
 카. 조사 기획서는 획일적이어도 좋다. 
 타. 조사 방법을 알아두자. 
 파. 비용을 명확하게 해두자. 
 카. 주장을 펼치는 것이 중요하다. 
(5) 제시 방법 
 가. 도안과 디자인도 기획서의 중요한 구성 요소다. 
 나. 비주얼은 아주 강력한 호소력을 가진다. 
 다. 소제목과 내용은 유연하게 구성한다. 
 라. 기획서를 보기 쉽게 만드는 핵심은 소제목을 붙이는 것이다. 
 마. 제목으로 어필하는 것도 때로는 매우 중요하다. 
(6) 설득 
 가. 조사 결과와 데이터는 설득력을 가진다. 
 나. 채산성과 수치는 아주 중요하다. 
 다. 메리트가 떠오르게 하자. 
 라. 메리트가 많은 기획서는 쉽게 통과된다. 
 마. 결과를 분명하게 한다. 결과를 기대하게 하는 기획서를 작성한다.  
 바. 새로운 시도를 제안할 때에는 실시 효과를 강조한다.   
 사. 스스로 조사를 실행해 보자. 
(7) 종합기획서 
 가. 종합기획서에는 복수의 실행 계획을 조합한다.   
 나. 페이지 수가 많은 경우에는 [목차]를 붙인다. 
 다. [분석, 포지셔닝, 문제와 기획] 부분이 중요하다. 
 라. 기획서를 제시하는 방법에 변화를 주어도 좋다.  
 마. 프레젠테이션할 것이란 점을 고려하여 작성한다. 
 바. 경쟁 프레젠테이션에서의 평가는 기획서의 내용이 전부가 아니다.  
 사. 영문 기획서도 기본 구성은 동일하다.  
