USN(Ubiquitous Sensor Network): 전자태크(RFID)와 U-센서를 BcN과 연계하여 사물의 정보를 인식, 관리 하는 네트워크. 
BCN(Broadband Convergence Network): 음성ㆍ데이터, 유무선 등 통신ㆍ방송ㆍ인터넷이 융합된 품질 보장형  
광대역 멀티미디어 서비스를 언제 어디서나 끊김 없이(Seamleass) 안전하게 이용할 수 있는 차세대 통합 네트워크.  
ITS(Intelligent Transportation Systems): 지능형 교통 시스템. 전자, 정보, 통신, 제어 등의 기술을 교통체계에  
접목시킨 지능형 교통 시스템이다. 신속, 안전, 쾌적한 차세대 교통체계를 만드는데 목적을 두고 있다. 
ISP(Information Strategic Planning): 최적의 정보화(Information)를 추진해 나가기 위한 중장기 전략(Strategic)  
계획(Planning)을 수립 하는 것. 
CFAMS(Computerized Fire Alarm Monitoring System): 개별 건물/상가/공자의 화재 경보 상태를 모니터링하고 
비정상적인 상태가 발생할 경우 다이얼 호출식의 전화선(PSTN)을 통해 관제센터로 그 내용을 전송할 모니터링 
시스템의 구축하는 것. 
DAC(Digital Alarm Communicator) 
텔레매틱스(Telematics): 자동차와 무선통신을 결합한 새로운 개념의 차량 무선 인터넷 서비스 
BIS(Bus Information System): 버스정보안내 시스템 
BMS(Bus Management System): 버스운행정보 시스템 
FTTH(Fiber-To-The Home)전화국으로부터 각 가정까지 개별적으로 광섬유를 부설하여 광대역 서비스를 하는  
가입자망 방식. 전화, 팩스, 데이터, 텔레비전 영상의 광대역 서비스에 이르는 모든 통신 서비스가 한 줄의 광섬유로  
이루어질 수 있다. 그러나 파이버 투 더 홈(FTTH)은 각 가정마다 광섬유를 부설하고, 광망 종단 장치(ONU)도 개별적 
으로 설치해야 하는 등 경제성에 문제가 있다. 따라서 이미 사용 중인 광대역 방식을 활용하는 파이버 투 더 존(FTTZ)  
방식이 제안되었다. 
