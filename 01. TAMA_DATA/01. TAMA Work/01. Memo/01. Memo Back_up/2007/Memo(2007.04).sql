- 속독 
속독이란 '빠르게 해석하기' 입니다. 우리는 독서를할때 읽는다는 개념을 가지고 책일 보지만 
시를 뺀 나머지들.. 특히 설명문, 논설문 등은 읽기는 필요없습니다. 
눈 운동 -> 뇌(집중)  ==> 고속독해의 꽃은 눈의 움직임 
정확하지 않다면 빠르게 읽는 것이 무슨 소용없음 
- 속독은 순간적으로 빠르게 해석하는것 
우리가 글을 읽는건 머리로 얼마나 빠르게 받아들이느냐 이지, 글을 읽는게 아닙니다. 
머리가 받아들였다고 생각한다면 바로바로 글을 계속 이어나가는게 좋습니다. 
또한 속독은 암기력을 올려주진 않습니다. 암기력향상은 꾸준한 암기와 시간투자겠네요.. 
속독은 글을 빠르게 이해하여 머릿속으로 해석하는것이지, 빨리 외우는게 아닙니다. 
암기라고 할거같으면 읽고,쓰고,말하고 라고 할 수 있겠지만 
속독은 처음에 말했듯이 읽는게 아니라 '해석'입니다. 따라서 암기에 많은 도움을 주진 못합니다. 
빨리 읽을 수 있다고해서 암기를할땐 속독이 안쓰입니다. 
단어를 외울때는 확실히 그렇구요, 책 내용을 외우고 익힐땐 반복 속독도 괜찮을 수 있겠네요.  
