2. 삭제방법입니다. 
ⓐ 편집기를 연 상태에서,,,Ctrl + F 를 누르고, adspider (대/소문자 구별 없음)를 입력하고 엔터칩니다. 
나오는 것을 잘 확인한 후 짙은 색으로 표시된 부분을 우클릭하여 삭제합니다.
ⓑ 그 상태에서 F3 를 누릅니다. 나오는 것을 역시 잘 확인한 후에 삭제합니다.
ⓒ '더 이상 일치하는 ＠?찾을 수 없습니다' 란 말이 나올 때까지 F3 작업을 계속합니다.
ⓓ 다시,,,Ctrl + F 를 누르고 다잡아를 입력하여...ⓐ~ⓒ의 작업을 해줍니다.
ⓔ 편집기를 닫고 재부팅합니다.

