ⓓ Ad-Watch ; http://www.eince.com/bbs/zboard.php?id=AdWatch_PDS  

실시간 감시(자동실행) 기능이 있는 것들의 경우에도, 굳이 실시간으로 운영할 필요는 없고...대신에 정기적으로
