잘못하면 컴터가 다운되고 윈도우를 새로 깔아야 하는 불상사가 생길 수 있습니다.
따라서 사전에 미리 기존 레지 기록을 백업(저장)해 두는 것이 좋습니다.
시작-실행-regedit 입력-확인 ⇒ 레지스트리 편집기를 엽니다.
XP의 경우,,,좌측 상단의 '내컴퓨터'를 마우스 우클릭하여 '파일로 내보내기'를 선택합니다.

