윈도우에선 adspider,다잡아(comma로 연결하여 두 개 모두 함께 입력하여 검색할 수 있음)
를 검색한 뒤,  잘 확인하고,,,Shift키를 이용하여 전부 선택을 하여 삭제하였습니다.

Shift키를 이용하여 삭제하면 휴지통에도 못들어가고 완전히 삭제됩니다.

* 위의 삭제방법은 다른 경우에도 이용하시기 바랍니다.
