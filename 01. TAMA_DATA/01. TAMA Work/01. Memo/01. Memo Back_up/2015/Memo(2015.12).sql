- AD-Spider 처리
0. AD-Spiderf 정의
AD-Spider 란 이름을 사용하는 프로그램은 일명 "다잡아" 란 프로그램입니다. 
인터넷 웹서핑을 하다가 악성코드나 스파이 웨어가 사용자의 허락 없이 설치가 되곤하여 
컴퓨터의 성능을 저하 시키거나 시작페이지를 바꿔 놓는다거나 갑자기 광고 팝업창이 
뜨게 되곤 하는것을 제거하여 주는 프로그램입니다. 

실행에서 msconfig 를 하시면 시작관리자 창이 나타나게 됩니다. 
가장 오른쪽의 탭부분을 클릭하시면 부팅이후 실행이 되는 프로그램들이 나열되어 있습니다.  
앞의 체크박스 부분의 V를 없애 주시면 부팅시 시작되지 않습니다.  
시작프로그램 목록(시작-실행-msconfig 입력-시작프로그램

1. 일단 작업관리자에서 그 프로세스를 중지시키고, 
2. 시작프로그램 목록에 이상한 넘이 있는지 확인하여 실행 중지. 
3. 제어판-프로그램 추가/제거에서 이상한 넘이 있으면 삭제 (신중하게 판단하세요). 
4. (만약 저라면,,,) 그 파일을 우클릭하여 속성(등록정보)을 보고,,,괜찮다고 판단되면 삭제해 보았을겁니다.
5.  adspider를 검색해낸 프로그램이 어느건지 정확한 기억이 안나는데,,,다음 네 개 중에 하나입니다. 컴터에 
설치하여 검사/치료해 보십시오. 
